package exec2;

/**
 * Classe para demonstrar as operações
 * 
 * @author jaraujo
 * @since 11/02/2020 version 0.1
 */
public class Operacoes {
	public static void main(String[] args) {
		// exemplo de adição
		int adicao = 10 + 10;
		
		// exemplo de substração
		int subtracao = 10 - 10;
		
		// exemplo de multiplicação
		int multiplicacao = 10 * 10;
		
		// exemplo de divisão
		int divisao = 10 / 10;
		
		// exemplo de módulo
		// Equivale a 10 dividido por 3 que dá com resto 1
		// o módulo devolve o valor do resto
		int modulo = 10 % 3;

		System.out.println("10 + 10 = " + adicao);
		System.out.println("10 - 10 = "+subtracao);
		System.out.println("10 * 10 = "+multiplicacao);
		System.out.println("10 / 10 = "+divisao);
		System.out.println("10 % 10 = "+modulo);

	}
}
